class getnum:
    def __init__(self):
        self.n1 = int(input("Enter first num"))
        self.n2 = int(input("Enter second num: "))
class sum(getnum):
    def sum(self):
        print(self.n1 + self. n2)
class sub(getnum):
    def sub(self):
        print(self.n1 - self.n2)
class mul(getnum):
    def mul(self):
        print(self.n1 * self.n2)
class div(getnum):
    def div(self):
        print(self.n1 / self.n2)
